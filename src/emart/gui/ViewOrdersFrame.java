    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        //Deleteeee
         String orderId = jcOrderId.getSelectedItem().toString();
    try {
        ArrayList<ProductsPojo> orderDetails = OrderDAO.getOrderDetails(orderId);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        double total = 0;
        for (ProductsPojo product : orderDetails) {
            model.addRow(new Object[]{
                product.getProductId(),
                product.getProductName(),
                product.getProductCompany(),
                product.getProductPrice(),
                product.getOurPrice(),
                product.getQuantity(),
                product.getTax(),
                product.getTotal()
            });
            total += product.getTotal();
        }
        txtTotal.setText(String.valueOf(total));
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error fetching order details: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnShowActionPerformed

