    private void loadOrderIds() {
        try {
            ArrayList<String> orderIds = OrderDAO.getAllOrderIds();
            for (String orderId : orderIds) {
                jcOrderId.addItem(orderId);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error fetching order IDs: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
